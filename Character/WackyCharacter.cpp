void AWackyCharacter::ReceiveDamage(AActor* DamagedActor, float Damage, const UDamageType* DamageType, AController* InstigatorController, AActor* DamageCauser)
{
	WackyGameMode = WackyGameMode == nullptr ? GetWorld()->GetAuthGameMode<AWackyGameMode>() : WackyGameMode;
	if (bElimmed || WackyGameMode == nullptr) return;
	Damage = WackyGameMode->CalculateDamage(InstigatorController, Controller, Damage);

	float DamageToHealth = Damage;
	if (Shield > 0.f)
	{
		if (Shield >= Damage)
		{
			Shield = FMath::Clamp(Shield - Damage, 0.f, MaxShield);
			DamageToHealth = 0.f;
		}
		else
		{
			DamageToHealth = FMath::Clamp(DamageToHealth - Shield, 0.f, Damage);
			Shield = 0.f;
		}
	}

	Health = FMath::Clamp(Health - DamageToHealth, 0.f, MaxHealth);
	UpdateHUDHealth();
	UpdateHUDShield();
	PlayHitReactMontage();

	if (Health == 0.f)
	{
		if (WackyGameMode)
		{
			WackyPlayerController = WackyPlayerController == nullptr ? Cast<AWackyPlayerController>(Controller) : WackyPlayerController;
			AWackyPlayerController* AttackerController = Cast<AWackyPlayerController>(InstigatorController);
			WackyGameMode->PlayerEliminated(this, WackyPlayerController, AttackerController);
		}
	}
}


